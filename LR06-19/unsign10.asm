EXTRN NEW_LINE : NEAR
PUBLIC UNSIGNED10

DSEG SEGMENT PARA PUBLIC 'DATA'
  W DB  10,13,'UNSIGNED10', '$'
DSEG	ENDS

CSEG	SEGMENT PARA PUBLIC 'CODE'
		ASSUME CS:CSEG, DS:DSEG

UNSIGNED10 PROC NEAR
  MOV AX,4
  MOV AH,9
  MOV DX, OFFSET W
  INT 21h
  CALL NEW_LINE
  RET
UNSIGNED10 ENDP

CSEG	ENDS
END
